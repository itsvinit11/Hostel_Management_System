    private void displayCategory()
    {
        try
        {
            jComboBox1.removeAllItems();
            Connection con=connection_provider.getCon();
        Statement st=con.createStatement();
        ResultSet rs=st.executeQuery("select *from categoryInfo");
        while(rs.next())
        {
            jComboBox1.addItem(rs.getString("category"));
        }
        }
        catch(Exception e)
        {
           JOptionPane.showMessageDialog(null, e);
        }
    }
    private void getEntries()
    {
        try
        {
            javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel)jTable1.getModel();
            int rc=dtm.getRowCount();
            while(rc-- !=0)
        {
            dtm.removeRow(0);
        }
            java.time.LocalDate cd=java.time.LocalDate.now();
            java.time.LocalDate bd=cd.minusDays(20);
            Connection con=connection_provider.getCon();
        Statement st=con.createStatement();
        ResultSet rs=st.executeQuery("select *from spendings where date<='"+cd+"' and date>='"+bd+"'");
        int total = 0;
        int sid =0;
        while(rs.next())
        {
            int t = rs.getInt("amount");
            sid=rs.getInt("sId");
            total+=t;
            Object o[]={sid,rs.getDate("date"),rs.getString("category"),t};
            dtm.addRow(o);
        }
        jLabel7.setText(total+"");
        }
        catch(Exception e)
        {
             JOptionPane.showMessageDialog(null, e);
        }
    }

