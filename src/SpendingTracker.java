    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int ri=jTable1.getSelectedRow();
        if(ri!=-1)
        {
            int r=JOptionPane.showConfirmDialog(null,"Do You Really Want to Delete?","Deletion Confirmation",JOptionPane.YES_NO_OPTION);
            if(r==JOptionPane.YES_OPTION)
            {
        int id=(int)jTable1.getValueAt(ri, 0);
        try
        {
            
        Connection con=connection_provider.getCon();
        Statement st=con.createStatement();
        st.executeUpdate("delete from spendings where sId='"+id+"'");
        JOptionPane.showMessageDialog(null," deleted successfully");
        getEntries();
        clear();
        
        }
        catch(Exception e)
                {
                  JOptionPane.showMessageDialog(null,e);  
                }
        }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

