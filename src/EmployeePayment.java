    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String mobileNo=jTextField1.getText();
        SimpleDateFormat dFormat=new SimpleDateFormat("MMM-YYYY");
        Date date=new Date();
        String month=dFormat.format(date);
        try
        {
            Connection con=connection_provider.getCon();
        Statement st=con.createStatement();
        ResultSet rs=st.executeQuery("select *from employee where mobileNo='"+mobileNo+"' and status='working'");
        if(rs.next())
        {
            jTextField1.setEditable(false);
            jTextField2.setText(rs.getString(2));
            jTextField3.setText(rs.getString(5));
            jTextField4.setText(rs.getString(8));
            jTextField5.setText(month);
            jTextField6.setText("10000");
            
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Employee does not Exist");
            clear();
        }
        tableDetails();
        
        ResultSet rs1=st.executeQuery("select *from payment inner join employee where employee.status='working' and payment.month='"+month+"' and payment.mobileNo='"+mobileNo+"'and employee.mobileNo='"+mobileNo+"'");
        if(rs1.next())
        {
            jButton3.setVisible(false);
            tableDetails();
            JOptionPane.showMessageDialog(null,"Payment is already paid to the employee for this month");
        }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String mobileNo=jTextField1.getText();
        String month=jTextField5.getText();
        String amount=jTextField6.getText();
        try
        {
             Connection con=connection_provider.getCon();
             PreparedStatement ps=con.prepareStatement("insert into payment values(?,?,?)");
              ps.setString(1, mobileNo);
            ps.setString(2, month);
            ps.setString(3, amount);
            ps.executeUpdate();
            tableDetails();
            JOptionPane.showMessageDialog(null,"Successfully Updated");
            clear();
        }
        catch(Exception e)
                {
                  JOptionPane.showMessageDialog(null,e);  
                }
    }//GEN-LAST:event_jButton3ActionPerformed

